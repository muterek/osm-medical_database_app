package projekt2MR;


public class pacjent {
	
	private String imie, nazwisko, pesel;
	
	public pacjent(String imie, String nazwisko, String pesel) {
		this.imie = imie;
		this.nazwisko = nazwisko;
		this.pesel = pesel;
	}
	
	public String getImie() {
		return imie;
	}
	
	public String getNazwisko() {
		return nazwisko;
	}
	
	public String getPesel() {
		return pesel;
	}
	
	

}
